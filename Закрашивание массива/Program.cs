void PrintImage(int[,] image)
{
 for (int i = 0; i < image.GetLength(0); i++)
 {
 for (int j = 0; j < image.GetLength(1); j++)
 {
 if(image[i,j] == 0) Console.Write($" ");
 else Console.Write($"+");
 }
 Console.WriteLine();
 }
}
void FillImage(int row, int col) // Указываем строку и пиксели с которых должны закрашивать
{
 if (pic[row, col] == 0) //Если текущий пиксель с указанной позицие row , col равен нулю
 {
 pic[row, col] = 1;
 FillImage(row - 1, col); //Вверх
 FillImage(row, col - 1); //Влево
 FillImage(row + 1, col); //Вниз
 FillImage(row, col + 1); //Вправо
 }
}
